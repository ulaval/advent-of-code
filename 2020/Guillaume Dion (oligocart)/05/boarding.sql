create or replace function bin2dec (p_binary in varchar2) return pls_integer is
    l_value pls_integer := 0;
begin
    for i in 1..length(p_binary) loop
        l_value := 2 * l_value + to_number(substr(p_binary,i,1));
    end loop;
    return l_value;
end;
/

create or replace view plane_seat as 
with seat_data as (select seat, translate(seat, 'BRFL', '1100') bin from boarding)
select 8*bin2dec(substr(bin,1,7)) + bin2dec(substr(bin,8)) seat_id from seat_data;

-- Part 1
select max(seat_id) part1_answer from plane_seat;

-- Part 2
with seat_ordering as (select seat_id, lag(seat_id) over (order by seat_id) seat_before, lead(seat_id) over (order by seat_id) seat_after from plane_seat)
select avg(seat_id) part2_answer from seat_ordering where seat_before < (seat_id-1) or seat_after > (seat_id+1);
