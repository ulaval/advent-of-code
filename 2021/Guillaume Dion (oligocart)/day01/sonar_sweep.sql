select sum, prev_sum, case when sum > prev_sum then 'increased' else 'decreased' end state from (
with sums as (select id, depth + lag(depth, 1) over (order by id) + lag(depth, 2) over (order by id) sum from sonar_sweep)
select sum, lag(sum) over (order by id) prev_sum from sums
)) where state = 'increased';
